// Three.js 3D Background

const canvas = document.getElementById('bg-canvas');

if (canvas && typeof THREE !== 'undefined') {

    const scene = new THREE.Scene();

    const camera = new THREE.PerspectiveCamera(75, window.innerWidth / window.innerHeight, 0.1, 1000);

    const renderer = new THREE.WebGLRenderer({ canvas: canvas, alpha: true });


    renderer.setSize(window.innerWidth, window.innerHeight);

    renderer.setPixelRatio(window.devicePixelRatio);

    camera.position.z = 5;



    // Create particles

    const particlesGeometry = new THREE.BufferGeometry();

    const particlesCount = 1000;

   const posArray = new Float32Array(particlesCount * 3);



    for (let i = 0; i < particlesCount * 3; i++) {

       posArray[i] = (Math.random() - 0.5) * 10;

   }



    particlesGeometry.setAttribute('position', new THREE.BufferAttribute(posArray, 3));

    const particlesMaterial = new THREE.PointsMaterial({

        size: 0.02,

        color: 0xf5f5f5,

        transparent: true,

        opacity: 0.8,

        blending: THREE.AdditiveBlending

    });



    const particlesMesh = new THREE.Points(particlesGeometry, particlesMaterial);

    scene.add(particlesMesh);


    // Create rotating geometry

    const geometry = new THREE.TorusKnotGeometry(2, 0.5, 100, 20)
    const material = new THREE.MeshBasicMaterial({

        color: 0xf5f5f5,

        wireframe: true,
        transparent: true,
        opacity: 0.3

    });
    const torusKnot = new THREE.Mesh(geometry, material);
    scene.add(torusKnot);

    // Mouse movement effect

    let mouseX = 0;
    let mouseY = 0;
   document.addEventListener('mousemove', (event) => {

        mouseX = (event.clientX / window.innerWidth) * 2 - 1;

       mouseY = -(event.clientY / window.innerHeight) * 2 + 1;

    });



    // Animation loop

    function animate() {

        requestAnimationFrame(animate);



       // Rotate particles

        particlesMesh.rotation.y += 0.001;

        particlesMesh.rotation.x += 0.0005;



        // Rotate torus knot

        torusKnot.rotation.x += 0.01;

        torusKnot.rotation.y += 0.01;



        // Mouse interaction

        camera.position.x += (mouseX * 0.5 - camera.position.x) * 0.05;

        camera.position.y += (mouseY * 0.5 - camera.position.y) * 0.05;

        camera.lookAt(scene.position);



        renderer.render(scene, camera);

    }



    animate();



    // Handle window resize

    window.addEventListener('resize', () => {

        camera.aspect = window.innerWidth / window.innerHeight;

        camera.updateProjectionMatrix();

        renderer.setSize(window.innerWidth, window.innerHeight);

    });

}



// 3D Tilt Effect on Portfolio Items

const portfolioBoxes = document.querySelectorAll('.portfolio-box');

portfolioBoxes.forEach(box => {

    box.addEventListener('mousemove', (e) => {

        const rect = box.getBoundingClientRect();

        const x = e.clientX - rect.left;

        const y = e.clientY - rect.top;
        

        const centerX = rect.width / 2;

        const centerY = rect.height / 2;

        

        const rotateX = (y - centerY) / 10;

       const rotateY = (centerX - x) / 10;

        

        box.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) translateZ(20px)`;

    });

    

    box.addEventListener('mouseleave', () => {

        box.style.transform = 'perspective(1000px) rotateX(0) rotateY(0) translateZ(0)';

    });

});



