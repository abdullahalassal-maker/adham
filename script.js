const langSwitch = document.getElementById('langSwitch');
const langBox = document.querySelector('.lang-toggle');

function setLanguage(lang) {
    document.querySelectorAll('[data-i18]').forEach(el => {
        const key = el.getAttribute('data-i18');
        if(translations[lang][key]){
            el.textContent = translations[lang][key];
        }
    });

    if(lang === 'ar') {
        document.body.dir = 'rtl';
        document.documentElement.lang = 'ar';
        langSwitch.checked = false;
        langBox.style.left = '20px';
        langBox.style.right = 'auto';
    } else {
        document.body.dir = 'ltr';
        document.documentElement.lang = 'en';
        langSwitch.checked = true;
        langBox.style.right = '20px';
        langBox.style.left = 'auto';
    }

    localStorage.setItem('siteLanguage', lang);
}

// تحميل اللغة المحفوظة مسبقًا
const savedLang = localStorage.getItem('siteLanguage') || 'en';
setLanguage(savedLang);

// عند تبديل السلايدر
langSwitch.addEventListener('change', () => {
    if(langSwitch.checked) {
        setLanguage('en');
    } else {
        setLanguage('ar');
    }
});




